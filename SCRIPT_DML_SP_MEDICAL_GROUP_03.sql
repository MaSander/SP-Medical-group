USE SP_MEDICAL_GROUP

SELECT * FROM MEDICOS

SELECT
	U.NOME as Nome_do_usuario
	,U.EMAIL
	,TU.NOME as Tipo_do_usuario
FROM 
	USUARIOS U INNER JOIN TIPO_USUARIO TU
ON
	U.ID_TIPO_USUARIO = TU.ID

------------------------

SELECT
	U.NOME AS Nome_paciente
	,P.TELEFONE
	,P.ENDERECO
	,P.DT_NASCIMENTO as Data_Nascimento
	,P.RG
	,P.CPF
FROM
	PRONTUARIOS P 
INNER JOIN 
	USUARIOS U
ON
	P.ID_USUARIO = U.ID

--------------------------

